#!/bin/bash
#############################################################
#SCHEDULER OPTIONS
#############################################################
#batch

#BASH SCRIPT IS ASSUMED
#############################################################
#PREPARE SCRIPT
#############################################################
. sci2web/bin/sci2web.sh
. sci2web/runpipeline.conf
checkSig 'run'
setSig 'start'

#############################################################
#RUN COMMAND
#############################################################
(make run >> stdout.oxt 2>> stderr.oxt) & 

#############################################################
#GET PROCESS ID
#############################################################
PID=$!
echo $PID > pid.oxt
wait 

#############################################################
#FINISH PROCESS
#############################################################
if [ -e 'end.sig' ];then setSig 'end'
else
    if [ ! -e 'pause.sig' -a ! -e 'stop.sig' -a ! -e 'kill.sig' ]
    then setSig 'fail'
    fi
fi
#############################################################
#POST EXECUTION ACTIONS
#############################################################
bash sci2web/bin/sci2web.sh post
